namespace FontStashSharp
{
	/// <summary>
	/// Specifies text styling options for displayed text.
	/// </summary>
	public enum TextStyle
	{
		/// <summary>
		/// No text styling applied.
		/// </summary>
		None,
		/// <summary>
		/// Text is underlined.
		/// </summary>
		Underline,
		/// <summary>
		/// Text is struck through.
		/// </summary>
		Strikethrough
	}
}
